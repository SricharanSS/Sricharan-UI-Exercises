const notesContainer = document.getElementById("note-container");

const noteDisplay = document.getElementById("note-display");

const backbtn = document.getElementById("back-btn");

const loadMoreBtn = document.getElementById("load-more-btn");


/* Button Elements */ 
const deleteBtn = document.getElementById("delete-btn");
const deleteAllBtn = document.getElementById("delete-all-btn");
const editBtn = document.getElementById("edit-btn");
const newBtn = document.getElementById("new-btn");
const addBtn = document.getElementById("add-btn");


/* Event Listener for New Button */
newBtn.addEventListener("click", function() {
    document.getElementsByClassName("modal")[0].style.display = "inline-block";
    document.getElementById("new-modal").style.display = "block";
}); 

/* Event Listener for Closing Add New Note Modal */
const newModal = document.getElementById("new-modal");
newModal.addEventListener("click", function(event) {
    event.preventDefault();
    if(event.target.tagName === 'I') {
        document.getElementsByClassName("modal")[0].style.display = "none";
        document.getElementById("new-modal").style.display = "none";
    }
});

/* Event Listener for Edit Button */
editBtn.addEventListener("click", function() {
    document.getElementsByClassName("modal")[0].style.display = "inline-block";
    document.getElementById("edit-modal").style.display = "block";
});

/* Event Listener for Closing Edit Note Modal */ 
const editModal = document.getElementById("edit-modal");
editModal.addEventListener("click", function(event) {
    event.preventDefault();
    if(event.target.tagName === "I") {
        document.getElementsByClassName("modal")[0].style.display = "none";
        document.getElementById("edit-modal").style.display = "none";
    }
});


/* GET Title */
let title;
document.getElementById("note-title").addEventListener("blur", function(event) {
    title = event.target.value;
});

/* GET Content */ 
let content;
document.getElementById("note-content").addEventListener("blur", function(event) {
    content = event.target.value;
});

/* Event Listener for Add Button */
const addNoteForm = document.getElementById("add-note-form");
addNoteForm.addEventListener("click", function(event) {
    if(event.target.tagName === "BUTTON") {
        let date = "Jan 02, 2001";
        notes.unshift({"title" : title, "date" : date, "content": content});
        document.getElementsByClassName("modal")[0].style.display = "none";
    }
    LoadNotes();
});


/* Load More Button Functionality */ 
loadMoreBtn.addEventListener('click', function() {
    LoadNotes();
    for(let i=0; i<notes.length; i++) {
        $("#note-"+i).on("click",function() {
            notesContainer.style.display = "none";
            backbtn.style.display = "inline-block";
            loadMoreBtn.style.display = "none";

            deleteAllBtn.style.display = "none";
            newBtn.style.display = "none";
            deleteBtn.style.display = "inline-block";
            editBtn.style.display = "inline-block";

            noteDisplay.innerHTML = "<div id='note-"+i+"'> <h2>"+notes[i].title+"</h2> <p class='date' >"+notes[i].date+"</p> <p class='content' > "+notes[i].content+"</p> </div>";
        });
    }
});

/* Load Notes */ 
let index = 0;
let flag = 0;
function LoadNotes() {
    let count = 0;
    while(count < 10 && (index !== notes.length)) {
        flag = 1;
        notesContainer.innerHTML += "<div id='note-"+index+"' class='note'> <h2>"+notes[index].title+"</h2> <p class='date' >"+notes[index].date+"</p> <p class='content' > "+notes[index].content+"</p> </div>";
        index++;
        count++;
    }

    if(index === notes.length) {
        loadMoreBtn.style.display = "none";
    }
}

$("#back-btn").on("click",function() {
    noteDisplay.innerHTML = "";
    backbtn.style.display = "none";
    loadMoreBtn.style.display = "initial";
    notesContainer.style.display = "flex";

    deleteAllBtn.style.display = "initial";
    newBtn.style.display = "initial";
    deleteBtn.style.display = "none";
    editBtn.style.display = "none";

});

